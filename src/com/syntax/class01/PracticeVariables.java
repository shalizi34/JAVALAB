package com.syntax.class01;

public class PracticeVariables {

	public static void main(String[] args) {
		
		//to store whole numbers
		
		byte king=12;
		
		short king01=1232;
		
		int king02=2340;
		
		long king03=21234;
		
		//to store decimal numbers 
		
		float king04=13.78f;
		
		double king05=16.5443;
		
				
//to store single character
		
		char king06='M';
		
		//to store boolean values
		
		boolean Absent=true;
		boolean Present=false;
		
		System.out.println(king);
		System.out.println(king01);
		System.out.println(king02);
		System.out.println(king04);
		System.out.println(king05);
		System.out.println(king06);
		
	}

}
